class Malaysia(Country):
    country_name = "Malaysia"
    language = "Malay"
    pop_total = 31.95 # in millions
    pop_density = 95.96 # per square kilometer

    def __init__(self):
        pass

    def get_country_name(self):
        return country_name

    def get_language(self):
        return language

    def set_language(self, language):
        this.language = language

    def get_population_total(self):
        return pop_total

    def set_population_total(self, pop_total):
        this.pop_total = pop_total

    def get_population_density(self):
        return pop_density

    def set_population_density(self, pop_density):
        this.pop_density = pop_density